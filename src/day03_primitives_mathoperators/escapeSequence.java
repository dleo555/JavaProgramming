package day03_primitives_mathoperators;

public class escapeSequence {
    public static void main(String [] args){
        // \t - tab
        System.out.println("I can code\t\tJava");
        // \n - new line
        System.out.println("I can code \nJava");
        // \' - single quote
        System.out.println("I can code \'Java\'");
        // \" - double quote
        System.out.println("I can code \"Java\"");
        // \\ - single back slash
        System.out.println("/ | \\");
// assignment: print "\_/"
        System.out.println("\"\\_/\"");
    }
}
